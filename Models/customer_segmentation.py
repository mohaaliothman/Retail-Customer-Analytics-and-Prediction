import pandas as pd
from sklearn.preprocessing import StandardScaler
from sklearn.cluster import KMeans
from sklearn.metrics import silhouette_score

# 1. Load clean orders
df = pd.read_csv("clean_orders.csv")

# 2. Convert date
df["InvoiceDate"] = pd.to_datetime(df["InvoiceDate"])

# 3. Create snapshot date
snapshot_date = df["InvoiceDate"].max() + pd.Timedelta(days=1)
    #Take the last purchase date in the data and add one day.

# 4. Build RFM table
rfm = df.groupby("Customer ID").agg(
    Recency=("InvoiceDate", lambda x: (snapshot_date - x.max()).days),
    Frequency=("Invoice", "nunique"),
    Monetary=("TotalPrice", "sum")
).reset_index()

# 5. Scale features
features = ["Recency", "Frequency", "Monetary"]
scaler = StandardScaler()
X_scaled = scaler.fit_transform(rfm[features])

# 6. Train K-Means
kmeans = KMeans(n_clusters=5, random_state=42, n_init=10)
rfm["Cluster"] = kmeans.fit_predict(X_scaled)

# 7. Evaluate model
score = silhouette_score(X_scaled, rfm["Cluster"])
print("Silhouette Score:", round(score, 3))

cluster_summary = rfm.groupby("Cluster")[["Recency", "Frequency", "Monetary"]].mean()
cluster_summary = cluster_summary.rename(columns={
    "Recency": "Avg_Recency",
    "Frequency": "Avg_Frequency",
    "Monetary": "Avg_Monetary"
})
cluster_summary = cluster_summary.reset_index()
print(cluster_summary)

# 9. Add simple business labels
def label_cluster(row):
    # 1. VIP
    if row["Avg_Monetary"] > 100000:
        return "VIP Customers"

    # 2. Loyal
    elif row["Avg_Monetary"] > 20000 and row["Avg_Frequency"] > 50:
        return "Loyal Customers"

    # 3. Mid / Regular
    elif row["Avg_Frequency"] > 20:
        return "Regular Customers"

    # 4. At Risk
    elif row["Avg_Recency"] > 300:
        return "At-Risk Customers"

    # 5. Low Value
    else:
        return "Low Value Customers"


cluster_summary["Cluster_Label"] = cluster_summary.apply(label_cluster, axis=1)

# 10. Merge labels back to customers
rfm = rfm.merge(
    cluster_summary[["Cluster", "Cluster_Label"]],
    on="Cluster",
    how="left"
)

